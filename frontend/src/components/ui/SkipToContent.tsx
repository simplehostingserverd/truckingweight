'use client';

import React from 'react';

/**
 * SkipToContent component
 * 
 * This component provides a "Skip to Content" link that is visually hidden
 * but becomes visible when focused, allowing keyboard users to skip navigation
 * and go directly to the main content.
 */
export function SkipToContent() {
  return (
    <a
      href="#main-content"
      className="sr-only focus:not-sr-only focus:absolute focus:top-4 focus:left-4 focus:z-50 focus:px-4 focus:py-2 focus:bg-primary focus:text-primary-foreground focus:rounded-md focus:outline-none focus:shadow-md"
    >
      Skip to content
    </a>
  );
}
